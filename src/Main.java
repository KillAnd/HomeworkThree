public class Main {
    public static void main(String[] args) {

        byte oneTic = 67;
        short twoTic = 569;
        short c = -159;
        int threeTic = 27897;
        long forTic = 987678965549L;
        float fiveTic = 27.12f;
        double sixTic = 2.786;

        System.out.println("Значению " + oneTic + " присваиваю тип byte");
        System.out.println("Значению " + twoTic + " присваиваю тип short");
        System.out.println("Значению " + c + " присваиваю тип short");
        System.out.println("Значению " + threeTic + " присваиваю тип int");
        System.out.println("Значению " + forTic + " присваиваю тип long");
        System.out.println("Значению " + fiveTic + " присваиваю тип float");
        System.out.println("Значению " + sixTic + " присваиваю тип double");
    }
}